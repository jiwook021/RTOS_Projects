static uint8_t calculateChecksum(uint8_t data)
{
    return data ^ 0xFF;
}

/**
  * @brief Process SPI command received from host
  * @param command Command byte received
  * @param checksum Checksum byte received
  * @retval None
  */
/**
  * @brief Process SPI command received from host
  * @param command Command byte received
  * @param checksum Checksum byte received
  * @retval None
  */
void ProcessSPICommand(uint8_t command, uint8_t checksum)
{
  // Debug output
  char debugMsg[50];
  sprintf(debugMsg, "Processing CMD: 0x%02X, Checksum: 0x%02X\r\n", command, checksum);
  HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);

  /* Verify checksum */
  if (calculateChecksum(command) != checksum)
  {
    /* Invalid checksum */
    responseBuffer[0] = RESP_ERROR;
    responseBuffer[1] = calculateChecksum(RESP_ERROR);

    // Debug output
    sprintf(debugMsg, "Checksum error! Expected: 0x%02X\r\n", calculateChecksum(command));
    HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);

    // Update TX buffer immediately
    txBuffer[0] = responseBuffer[0];
    txBuffer[1] = responseBuffer[1];
    return;
  }

  /* Process valid command */
  switch (command)
  {
    case CMD_LED_ON:
      /* Turn LED ON - Using PJ1 as defined */
      HAL_GPIO_WritePin(SPI_LED_PORT, SPI_LED_PIN, GPIO_PIN_SET);
      spiLedState = 1;

      // Debug output
      HAL_UART_Transmit(&huart1, (uint8_t*)"LED ON (PJ1)\r\n", 14, 100);

      /* Prepare ACK response */
      responseBuffer[0] = RESP_ACK;
      responseBuffer[1] = calculateChecksum(RESP_ACK);
      break;

    case CMD_LED_OFF:
      /* Turn LED OFF */
      HAL_GPIO_WritePin(SPI_LED_PORT, SPI_LED_PIN, GPIO_PIN_RESET);
      spiLedState = 0;

      // Debug output
      HAL_UART_Transmit(&huart1, (uint8_t*)"LED OFF (PJ1)\r\n", 15, 100);

      /* Prepare ACK response */
      responseBuffer[0] = RESP_ACK;
      responseBuffer[1] = calculateChecksum(RESP_ACK);
      break;

    case CMD_QUERY_STATE:
      /* Prepare response with LED state */
      if (spiLedState)
      {
        responseBuffer[0] = RESP_LED_ON;
        responseBuffer[1] = calculateChecksum(RESP_LED_ON);
        HAL_UART_Transmit(&huart1, (uint8_t*)"Query: LED is ON\r\n", 18, 100);
      }
      else
      {
        responseBuffer[0] = RESP_LED_OFF;
        responseBuffer[1] = calculateChecksum(RESP_LED_OFF);
        HAL_UART_Transmit(&huart1, (uint8_t*)"Query: LED is OFF\r\n", 19, 100);
      }
      break;

    case CMD_READ_ANALOG:
        /* 아날로그 값 읽기 */
        HAL_ADC_Start(&hadc3);
        if (HAL_ADC_PollForConversion(&hadc3, 100) == HAL_OK)
        {
            uint16_t adcValue = HAL_ADC_GetValue(&hadc3);
            // 12비트(0-4095)에서 8비트(0-255)로 제대로 스케일링
            uint8_t scaledValue = (uint8_t)((adcValue * 255UL) / 4095UL);

            char debugMsg[50];
            sprintf(debugMsg, "ADC Raw: %u, Scaled: %u\r\n", adcValue, scaledValue);
            HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);

            responseBuffer[0] = scaledValue;
            responseBuffer[1] = calculateChecksum(scaledValue);

            // 바로 txBuffer에 복사하여 다음 SPI 전송 준비
            txBuffer[0] = responseBuffer[0];
            txBuffer[1] = responseBuffer[1];
        }
        else
        {
            responseBuffer[0] = RESP_ERROR;
            responseBuffer[1] = calculateChecksum(RESP_ERROR);
            txBuffer[0] = responseBuffer[0];
            txBuffer[1] = responseBuffer[1];
        }
        HAL_ADC_Stop(&hadc3);
        break;

    case CMD_GET_RESPONSE:
      /* No action needed - just return current response buffer */
      HAL_UART_Transmit(&huart1, (uint8_t*)"Get Response\r\n", 14, 100);
      break;


    default:
      /* Unknown command */
      responseBuffer[0] = RESP_ERROR;
      responseBuffer[1] = calculateChecksum(RESP_ERROR);
      // Debug output
      sprintf(debugMsg, "Unknown CMD: 0x%02X\r\n", command);
      HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);
      break;
  }

  // Update TX buffer immediately with the prepared response
  txBuffer[0] = responseBuffer[0];
  txBuffer[1] = responseBuffer[1];

  // Debug output of the response being sent
  sprintf(debugMsg, "Response: [0x%02X, 0x%02X]\r\n", txBuffer[0], txBuffer[1]);
  HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);
}

/**
  * @brief SPI transmission/reception complete callback
  * @param hspi: SPI handle
  * @retval None
  */
void HAL_SPI_TxRxCpltCallback(SPI_HandleTypeDef *hspi)
{
    if (hspi->Instance == SPI5)
    {
        static SPI_Message_t msg;

        // 디버그 출력
        char debugMsg[80];
        sprintf(debugMsg, "SPI IRQ: RX=[0x%02X, 0x%02X], TX was=[0x%02X, 0x%02X]\r\n",
                rxBuffer[0], rxBuffer[1], txBuffer[0], txBuffer[1]);
        HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);

        // 데이터 복사
        msg.command = rxBuffer[0];
        msg.checksum = rxBuffer[1];

        // 메시지 큐로 데이터 전송 (블로킹 없이)
        osStatus_t status = osMessageQueuePut(spiMsgQueue, &msg, 0, 0);
        if (status != osOK) {
            HAL_UART_Transmit(&huart1, (uint8_t*)"Queue error\r\n", 13, 100);
        }

        // 즉시 새로운 SPI 전송 설정 - 타이밍 중요
        HAL_SPI_TransmitReceive_IT(hspi, (uint8_t*)txBuffer, (uint8_t*)rxBuffer, SPI_BUFFER_SIZE);
    }
}

/**
  * @brief SPI Task function
  * @param argument: Not used
  * @retval None
  */
/**
  * @brief SPI Task function
  * @param argument: Not used
  * @retval None
  */
void SPI_Task(void *argument)
{
    /* Debug message via UART */
    HAL_UART_Transmit(&huart1, (uint8_t*)"SPI Task started\r\n", 18, 100);

    /* Ensure GPIOJ is enabled */
    __HAL_RCC_GPIOJ_CLK_ENABLE();

    /* Explicitly initialize PJ1 as output for LED control */
    GPIO_InitTypeDef GPIO_InitStruct = {0};
    GPIO_InitStruct.Pin = SPI_LED_PIN;  // PJ1
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
    HAL_GPIO_Init(SPI_LED_PORT, &GPIO_InitStruct);

    /* Initialize LED pin state */
    HAL_GPIO_WritePin(SPI_LED_PORT, SPI_LED_PIN, GPIO_PIN_RESET);
    spiLedState = 0;

    HAL_UART_Transmit(&huart1, (uint8_t*)"PJ1 configured as output\r\n", 26, 100);

    // Test toggling the LED to verify hardware
    HAL_GPIO_WritePin(SPI_LED_PORT, SPI_LED_PIN, GPIO_PIN_SET);
    HAL_UART_Transmit(&huart1, (uint8_t*)"PJ1 test HIGH\r\n", 15, 100);
    osDelay(200);
    HAL_GPIO_WritePin(SPI_LED_PORT, SPI_LED_PIN, GPIO_PIN_RESET);
    HAL_UART_Transmit(&huart1, (uint8_t*)"PJ1 test LOW\r\n", 14, 100);

    // Create message queue for handling commands between ISR and task
    spiMsgQueue = osMessageQueueNew(10, sizeof(SPI_Message_t), NULL);
    if (spiMsgQueue == NULL) {
        HAL_UART_Transmit(&huart1, (uint8_t*)"Error: Queue creation failed\r\n", 30, 100);
        Error_Handler();
    }

    // Initialize response buffer with processing status
    txBuffer[0] = responseBuffer[0] = RESP_PROCESSING;
    txBuffer[1] = responseBuffer[1] = calculateChecksum(RESP_PROCESSING);

    // Debug output
    char debugMsg[50];
    sprintf(debugMsg, "Initial TX: [0x%02X, 0x%02X]\r\n", txBuffer[0], txBuffer[1]);
    HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);

    // Start SPI in interrupt mode
    if (HAL_SPI_TransmitReceive_IT(&hspi5, (uint8_t*)txBuffer, (uint8_t*)rxBuffer, SPI_BUFFER_SIZE) != HAL_OK) {
        HAL_UART_Transmit(&huart1, (uint8_t*)"Error: SPI start failed\r\n", 25, 100);
        Error_Handler();
    }

    HAL_UART_Transmit(&huart1, (uint8_t*)"SPI initialized and running\r\n", 29, 100);

    SPI_Message_t receivedMsg;

    /* Main task loop */
    for(;;)
    {
        // Get message from queue (blocking with timeout)
        if (osMessageQueueGet(spiMsgQueue, &receivedMsg, NULL, 1000) == osOK)
        {
            // Debug output
            sprintf(debugMsg, "Queue RX: [0x%02X, 0x%02X]\r\n", receivedMsg.command, receivedMsg.checksum);
            HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);

            // Process command
            ProcessSPICommand(receivedMsg.command, receivedMsg.checksum);
        } else {
            // No message received in timeout period - can be used for periodic tasks
            // Check SPI peripheral status
            if (hspi5.State == HAL_SPI_STATE_READY || hspi5.State == HAL_SPI_STATE_ERROR) {
                HAL_UART_Transmit(&huart1, (uint8_t*)"SPI not active, restarting...\r\n", 31, 100);

                // Reset response and restart SPI
                txBuffer[0] = responseBuffer[0] = RESP_PROCESSING;
                txBuffer[1] = responseBuffer[1] = calculateChecksum(RESP_PROCESSING);

                HAL_SPI_TransmitReceive_IT(&hspi5, (uint8_t*)txBuffer, (uint8_t*)rxBuffer, SPI_BUFFER_SIZE);
            }
        }

        // Short delay to avoid consuming too much CPU
        osDelay(10);
    }
}

/**
  * @brief SPI error callback
  * @param hspi: SPI handle
  * @retval None
  */
void HAL_SPI_ErrorCallback(SPI_HandleTypeDef *hspi)
{
    if (hspi->Instance == SPI5)
    {
        // Log the error
        char debugMsg[50];
        sprintf(debugMsg, "SPI Error: %lu\r\n", hspi->ErrorCode);
        HAL_UART_Transmit(&huart1, (uint8_t*)debugMsg, strlen(debugMsg), 100);

        // Reset the SPI peripheral
        HAL_SPI_DeInit(&hspi5);
        HAL_SPI_Init(&hspi5);

        // Reset LED state
        spiLedState = 0;
        HAL_GPIO_WritePin(SPI_LED_PORT, SPI_LED_PIN, GPIO_PIN_RESET);

        // Prepare for next command
        txBuffer[0] = RESP_PROCESSING;
        txBuffer[1] = calculateChecksum(RESP_PROCESSING);

        // Re-enable SPI reception
        HAL_SPI_TransmitReceive_IT(hspi, (uint8_t*)txBuffer, (uint8_t*)rxBuffer, SPI_BUFFER_SIZE);

        HAL_UART_Transmit(&huart1, (uint8_t*)"SPI restarted\r\n", 15, 100);
    }
}

